/**
 * Collapsible sidebar navigation component.
 *
 * 64px when collapsed (icons only), 240px when expanded (icons + labels).
 * Uses Radix UI Tooltip for collapsed state hints.
 */
import { NavLink } from "react-router-dom";
import * as Tooltip from "@radix-ui/react-tooltip";
import { useLayoutStore } from "@/stores/layoutStore";
import { SidebarStatusIndicator } from "@/components/integrations";

// Navigation items configuration
const NAV_ITEMS = [
  { path: "/", icon: DashboardIcon, label: "QA Dashboard" },
  { path: "/pm-dashboard", icon: PMDashboardIcon, label: "PM Dashboard" },
  { path: "/tickets", icon: TicketsIcon, label: "Tickets" },
  { path: "/workflows", icon: WorkflowsIcon, label: "Workflows" },
  { path: "/patterns", icon: PatternsIcon, label: "Patterns" },
  { path: "/splunk", icon: SplunkIcon, label: "Splunk Logs" },
  { path: "/support", icon: SupportIcon, label: "Support" },
  { path: "/reports", icon: ReportsIcon, label: "Reports" },
  { path: "/settings", icon: SettingsIcon, label: "Settings" },
];

export function Sidebar() {
  const { sidebarCollapsed, toggleSidebar } = useLayoutStore();

  return (
    <aside
      className={`
        fixed left-0 top-0 h-screen bg-white border-r border-neutral-200
        flex flex-col transition-all duration-300 ease-in-out z-40
        ${sidebarCollapsed ? "w-16" : "w-60"}
      `}
    >
      {/* Logo */}
      <div className="h-16 flex items-center justify-center border-b border-neutral-200 px-3">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-lg bg-primary-500 flex items-center justify-center flex-shrink-0">
            <span className="text-white font-bold text-sm">QA</span>
          </div>
          {!sidebarCollapsed && (
            <span className="font-semibold text-neutral-900 whitespace-nowrap">
              QA PMS
            </span>
          )}
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 overflow-y-auto">
        <Tooltip.Provider delayDuration={0}>
          <ul className="space-y-1 px-2">
            {NAV_ITEMS.map((item) => (
              <li key={item.path}>
                <NavItemLink item={item} collapsed={sidebarCollapsed} />
              </li>
            ))}
          </ul>
        </Tooltip.Provider>
      </nav>

      {/* Integration Status */}
      <div className="border-t border-neutral-200">
        <SidebarStatusIndicator collapsed={sidebarCollapsed} />
      </div>

      {/* Collapse Toggle */}
      <div className="p-2 border-t border-neutral-200">
        <button
          type="button"
          onClick={toggleSidebar}
          className="w-full flex items-center justify-center gap-2 px-3 py-2.5
                     text-neutral-500 hover:text-neutral-700 hover:bg-neutral-100
                     rounded-lg transition-colors"
          title={`${sidebarCollapsed ? "Expand" : "Collapse"} sidebar (Ctrl+Shift+M)`}
        >
          {sidebarCollapsed ? (
            <ChevronRightIcon className="w-5 h-5" />
          ) : (
            <>
              <ChevronLeftIcon className="w-5 h-5" />
              <span className="text-sm">Collapse</span>
            </>
          )}
        </button>
      </div>
    </aside>
  );
}

interface NavItemLinkProps {
  item: (typeof NAV_ITEMS)[number];
  collapsed: boolean;
}

function NavItemLink({ item, collapsed }: NavItemLinkProps) {
  const Icon = item.icon;

  const link = (
    <NavLink
      to={item.path}
      end={item.path === "/"}
      className={({ isActive }) => `
        flex items-center gap-3 px-3 py-2.5 rounded-lg
        transition-colors duration-150
        ${isActive
          ? "bg-primary-50 text-primary-600"
          : "text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900"}
        ${collapsed ? "justify-center" : ""}
      `}
    >
      <Icon className="w-5 h-5 flex-shrink-0" />
      {!collapsed && <span className="font-medium">{item.label}</span>}
    </NavLink>
  );

  if (!collapsed) {
    return link;
  }

  return (
    <Tooltip.Root>
      <Tooltip.Trigger asChild>{link}</Tooltip.Trigger>
      <Tooltip.Portal>
        <Tooltip.Content
          side="right"
          className="bg-neutral-900 text-white text-sm px-3 py-1.5 rounded shadow-lg z-50"
          sideOffset={8}
        >
          {item.label}
          <Tooltip.Arrow className="fill-neutral-900" />
        </Tooltip.Content>
      </Tooltip.Portal>
    </Tooltip.Root>
  );
}

